# Define an empty list

fruits = []

# Define

vegetables = ["Carrot", "Peas"]

print(vegetables)

# Append an item to the list

fruits.append("Apple")
fruits.append("Banana")
fruits.append("Tomatoe")

print(fruits)

# Remove an item from a list

fruits.remove("Banana")
print(fruits)

# Remove item by index
del fruits[1]
print(fruits)
